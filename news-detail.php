                        <h3>Milan 2017</h3>
                        <p>NanSei had its first official promotional launch on the 12th of March at the campus of SRM University, Katangulathur, Tamil Nadu. MILAN being one of the grandest college cultural festival in the state, witnessed an estimate of 12,000 students attending the event. The event management and public relations team of NanSei, with a team of 16 members showed their support by managing the entire event at the host venue. The total budget for the event was Rs.9000. </p>
						<p>Team NanSei set up a camp at the host venue at 9.30 am and had initially put up stalls at one of the most famous eat outs within the college campus- JAVA Hub. Around 260 registrations were made within the first couple of hours. As the crowd at the canteen began to die down, team NanSei shifted its base in front of the MBA block. Students from various colleges and states were glad to register with NanSei and showed their support by clicking pictures and shooting videos with our team. Some students also extended their support by drawing attention to our stalls by performing their talents for us. At 5:00pm team NanSei once again moved its stalls and registration desk to the auditorium located within the campus of SRM where the team witnessed a different crowd of people who wished to join the organization. Our team not just caught the eyes on students from various parts of India, but also got fortunate enough to get a few registrations from students from Nigeria and Kenya. A total of 550 registrations were made. The event ended around 7:30 pm. </p>
